#include <stdio.h>

void main()
{
	/*
	%d,%i 输入整形
	%u, 输入无符号的十进制整数
	%o, 输入无符号的八进制整数
	%x,%X, 输入无符号的十六进制
	%c, 输入单个字符 char c = getchar();	// 65页 
	%s, 输入字符串
	%f, 输入实数, e/E, f,g/G
	----------------------------------
	附加字符
	l
		长整形数据: %ld %lo,%lx,%lu
		double型数据:%lf,%le 
	h
	域宽 例如下面的%2d里的2 
	* 表示在本输入项在读入后不赋给相应的变量
	
	输入实数的时候不能定义精度, 应先输入再取有效范围 
	*/
	int a,b;
	scanf("%2d%*3d%3d",&a,&b);
	printf("a=%d,b=%d",a,b);
	/*
		123456789
		a=12,b=678
		将两个整形数给a, 12 
		跳过3个整形数 345
		将接下来的3个整形数赋值给b, 678 
	*/
}
